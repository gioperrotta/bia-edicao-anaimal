function clickMenu() {
   if (menuMobile.style.display == "none") {
      menuMobile.style.display = "flex";
   } else {
      menuMobile.style.display = "none";
   }

   if (menuCelular.style.display == "none") {
      menuCelular.style.display = "flex";
   } else {
      menuCelular.style.display = "none";
   }
}
